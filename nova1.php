   <p>HTML é o formato padrão para criação de páginas online e aplicações de web. Em conjunto com o CSS e Javascript, eles formam as pedras principais para a World Wide Web. Todos os navegadores atuais recebem documentos em HTML que sao processados renderização e apresentação do conteúdo online. O nome HTML é uma abreviação para a expressão inglesa de HyperText Markup Language, que significa Linguagem de Marcação de Hipertexto.

A tecnologia é fruto da junção entre os padrões HyTime e SGML, que não são mais utilizados.

HyTime é um padrão para a representação estruturada de hipermídia e conteúdo baseado em tempo. Um documento é visto como um conjunto de eventos concorrentes dependentes de tempo (como áudio, vídeo, etc.), conectados por hiperligações. O padrão é independente de outros padrões de processamento de texto em geral.

SGML é um padrão de formatação de textos. Não foi desenvolvido para hipertexto, mas tornou-se conveniente para transformar documentos em hiper-objetos e para descrever as ligações.</p>
